// Scoring formula: trust = 0.5 * onChain + 0.3 * offChain + 0.2 * humanScore(default 50)
function computeTrustScore(onChain: number, offChain: number, human: number = 50): { score: number; grade: string } {
  const score = Math.round(0.5 * onChain + 0.3 * offChain + 0.2 * human);
  let grade: string;
  if (score >= 90) grade = 'S';
  else if (score >= 80) grade = 'A';
  else if (score >= 70) grade = 'B';
  else if (score >= 60) grade = 'C';
  else if (score >= 40) grade = 'D';
  else grade = 'F';
  return { score, grade };
}

