async function main() {
  console.log('🌱 Seeding 50 AI agents...\n');

  let created = 0;
  let updated = 0;

  for (const agent of agents) {
    const { score, grade } = computeTrustScore(agent.onChainScore, agent.offChainScore);

    const data = {
      name: agent.name,
      description: agent.description,
      image: agent.image,
      website: agent.website,
      category: 'm/ai-agents',
      chain: agent.chain,
      symbol: agent.symbol,
      tier: agent.tier,
      coreFunctions: JSON.stringify(agent.coreFunctions),
      twitter: agent.twitter,
      github: agent.github,
      docs: agent.docs,
      trustScore: score,
      trustGrade: grade,
      onChainScore: agent.onChainScore,
      offChainScore: agent.offChainScore,
      humanScore: 50, // default until reviews come in
      trustUpdatedAt: new Date(),
      marketCap: agent.marketCap,
      price: agent.price,
      volume24h: agent.volume24h,
      status: 'active',
    };

    const existing = await prisma.project.findUnique({ where: { address: agent.address } });

    if (existing) {
      await prisma.project.update({ where: { address: agent.address }, data });
      updated++;
    } else {
      await prisma.project.create({ data: { address: agent.address, slug: agent.address, ...data } });
      created++;
    }

    console.log(`  ${grade} ${score}/100 — ${agent.name} (${agent.chain})`);
  }

  console.log(`\n✅ Done: ${created} created, ${updated} updated`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
